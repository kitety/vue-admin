const mongoose = require('mongoose');

// Schema
const ProfileSchema = mongoose.Schema({
  type: {
    type: String
  },
  describe: {
    type: String
  },
  income: {
    type: String,
    required: true
  },
  expend: {
    type: String,
    required: true
  },
  cash: {
    type: String,
    required: true
  },
  remark: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});
// 注意本地collection  users
let Profile = module.exports = mongoose.model('profiles', ProfileSchema);
